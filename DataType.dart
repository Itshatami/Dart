void main() {
  // DataTypes
  // Strings , Numbers , Booleans , Lists , Maps , Dynamic

  // String
  String address = 'iran/golestan/gonbad';
  print("String: $address");

  // Integers
  int mobile = 9398482612;
  print("Integer: $mobile");

  // Double
  double price = 99.99;
  print("Double: $price");

  // Boolean
  bool isActive = false;
  print("Boolean: $isActive");

  // Dynamic
  dynamic id = "i dont know";
  print("Dynamic $id");
}
